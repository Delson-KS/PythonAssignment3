set1 = {1, 8, 2, 1, 7, 9}
set2 = {9, 2, 3 ,4 ,6}
setUnion = set1.union(set2)
setIntersection = set1.intersection(set2)
setDifference = set1.difference(set2)
setSymmetricDifference = set1.symmetric_difference(set2)
print("Union: " , setUnion ,'\n' , "Intersection: " ,setIntersection , '\n' , "Difference: ", setDifference , '\n' ,"Symmetric diff: " , setSymmetricDifference , '\n')
